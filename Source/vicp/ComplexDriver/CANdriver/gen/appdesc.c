static vuint32 SecM_GetRandomSeed(void)
{
    vuint16 seed_high_2byte = 0;
    vuint16 seed_low_2byte = 0;
    vuint32 seed = 0;

    /* get 16bit random value */
    srand(CanDriver_GetSeed());
    seed_high_2byte = rand();
    seed_low_2byte = rand();
    
    /* build a 32bit random value */
    seed = (((vuint32)seed_high_2byte) << 16) & 0xFFFF0000;
    seed |= ((vuint32)seed_low_2byte) & 0x0000FFFF;
    /* Seed must not be zero */
    if(0 == seed)
    {
        seed = 1;
    }

    return(seed);
}

/******************************************************************************
* Name         :  SecM_ComputeKey  
* Called by    :  CheckKey
* Preconditions:  None
* Parameters   :  None
* Description  :  
******************************************************************************/
#define MASK 0xEC20BE31
static vuint32 SecM_ComputeKey(void)
{
  unsigned int key=0; 
  
  unsigned char i;

  if(g_applDescSeed!=0)
  {
	  for(i=0; i<35; i++)
	  {
		  if(g_applDescSeed & 0x80000000)
		  {
			  g_applDescSeed = g_applDescSeed << 1;
			  g_applDescSeed = g_applDescSeed^MASK;
		  }
		  else
		  {
			  g_applDescSeed = g_applDescSeed << 1;
		  }
	  }
	  key = g_applDescSeed;
  }

  return (vuint32)key;
}



/******************************************************************************
* Name         :  SecM_Serializer  
* Called by    :  CheckKey
* Preconditions:  None
* Parameters   :  None
* Description  :  
******************************************************************************/
static void SecM_Serializer(DescMsg tgtPtr, vuint32 data)
{
  vuint8_least iter;
  iter = 4;
  while(iter != 0)
  {
    iter--;
    tgtPtr[iter] = (DescMsgItem)data;
    data >>= 8;
  }
}

